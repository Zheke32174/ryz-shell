#!/usr/bin/env python3
"""
ryznative — TRUE native backend for RYZ. Reuses ryzc's lexer/parser/AST and emits
C, then compiles with gcc to a standalone ELF (or a .so shared library). This brings
real native compilation to the canonical ryz-build ecosystem WITHOUT touching the
interpreter or the dialect — fully additive, all existing compatibilities preserved.

  ryznative <file.ryz>            -> ./<base>            native ELF (no interpreter)
  ryznative <file.ryz> --lib      -> ./lib<base>.so      shared library (export fn -> C ABI)
  ryznative <file.ryz> --emit-c   -> print generated C

Coverage: fn/export fn, let/mut, ints/floats/strings/bool, arithmetic + comparison +
and/or/not, if/else, while, for-in (list), return, break, continue, recursion,
fmt.println/print/sprintf, sys.argv/env/exit, list.len/push/get/range/new,
str.len/split/join/contains/replace/startswith/endswith/indexof, int()/str() casts.
"""
from __future__ import annotations
import importlib.util, importlib.machinery, os, subprocess, sys

HERE = os.path.dirname(os.path.abspath(__file__))
_loader = importlib.machinery.SourceFileLoader("ryzc", os.path.join(HERE, "ryzc"))
_spec = importlib.util.spec_from_loader("ryzc", _loader)
ryzc = importlib.util.module_from_spec(_spec)
sys.modules["ryzc"] = ryzc
_loader.exec_module(ryzc)  # type: ignore

class NativeError(Exception): pass

# ─── C runtime header embedded in every generated file ───────────────────────
